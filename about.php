                <h3>About Humanure Kenya</h3>
                <h5>Humanure Kenya is a non-profit organization that gives skills, training and dedicated to the teaching and promotion of ecological sanitation and composting.</h5>

                <h3>Our Vision</h3>
                <p>Healthy people, living in healthy houses, make healthy communities. That's our Vision. Right now, too many people in Kenya are suffering from easily preventable diseases such as cholera and dysentery. These illnesses can be stopped at the household level, by introducing home toilets, health education on good sanitation practices. Humanure Kenya is a community based organisation working with rural communities to build compost toilets in people’s homes and schools.</p>

                <h3>Our Mission</h3>
                <p>Seeking to put God’s love into action by bringing the community together and schools to a better healthy toilets, Humanure Kenya brings people together to build compost toilets in homes and schools. We also aim at creating job opportunities for the youths by equipping them with skills of building and carpentry, educating communities in rural areas and schools in 47 counties in Kenya. We view our work as successful when it transforms lives and promotes positive and lasting social, economic and spiritual change within a community; when it is based on mutual trust and fully shared accomplishment; and when it demonstrates responsible stewardship of all resources entrusted to us.

